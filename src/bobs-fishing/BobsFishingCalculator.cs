namespace BobsFishing.Calculator
{
    /// <summary>
    /// This is bobs fishing calculator. It does calculations.
    /// </summary>
    public class BobsFishingCalculator
    {
        /// <summary>
        /// Adds two numbers together.
        /// </summary>
        /// <param name="a">the first number.</param>
        /// <param name="b">The second number.</param>
        /// <returns>System.Int32.</returns>
        public int Add(int a, int b)
        {
            return a + b;
        }
    }
}